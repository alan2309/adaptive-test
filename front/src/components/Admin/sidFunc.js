function sidFunc(sid) {
  if (sid === 0) {
    return "Aptitude";
  } else if (sid === 1) {
    return "Computer Fundamentals";
  } else if (sid === 2) {
    return "Domain";
  } else if (sid === 3) {
    return "Personality";
  } else if (sid === 4) {
    return "Coding";
  } else if (sid === 5) {
    return "Analytical Writing";
  }
}
export default sidFunc;
